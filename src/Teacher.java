public class Teacher {
    int id;
    String name,email;

    //getter
    int getId(){
        return id;
    }
    String getName(){
        return name;
    }
    String getEmail(){
        return email;
    }

    void setId(int id){
        this.id=id;
    }
    void setName(String name){
        this.name=name;
    }
    void setEmail(String email){
        this.email=email;
    }
}


